# Data Types
## String
print("Hello")
print("Hello"[0]) # Subscript - Pull out a particular element from a string
print("Hello"[4]) # or
print("Hello"[-1])
print("123" + "345") # Concatenation

print("\n##########\n")

## Integer
print(123)
print(123 + 345)
print(123_456_789) # Use underscore to make large numbers more readable, only for understanding (123,456,789)

print("\n##########\n")

## Float
print(3.14159)
print(123.456)

print("\n##########\n")

## Boolean
print(True)
print(False)

